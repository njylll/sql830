package com.example.StudentDomain;


public class ClassInfo {

  private String uuid;
  private String classId;
  private String collegeId;
  private String majorId;
  private String counselorName;
  private String classTeacherName;
  private String monitorStudentId;


  public String getUuid() {
    return uuid;
  }

  public void setUuid(String uuid) {
    this.uuid = uuid;
  }


  public String getClassId() {
    return classId;
  }

  public void setClassId(String classId) {
    this.classId = classId;
  }


  public String getCollegeId() {
    return collegeId;
  }

  public void setCollegeId(String collegeId) {
    this.collegeId = collegeId;
  }


  public String getMajorId() {
    return majorId;
  }

  public void setMajorId(String majorId) {
    this.majorId = majorId;
  }


  public String getCounselorName() {
    return counselorName;
  }

  public void setCounselorName(String counselorName) {
    this.counselorName = counselorName;
  }


  public String getClassTeacherName() {
    return classTeacherName;
  }

  public void setClassTeacherName(String classTeacherName) {
    this.classTeacherName = classTeacherName;
  }


  public String getMonitorStudentId() {
    return monitorStudentId;
  }

  public void setMonitorStudentId(String monitorStudentId) {
    this.monitorStudentId = monitorStudentId;
  }

}
